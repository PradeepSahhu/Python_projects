
#Positional and Keyword Arguments.

def greet(name, your_name):
    print(f"Hello my name is {name}")
    print(f"How are you {your_name}")
    print(f"Would you like to have a drink? {your_name}! ")

#greet("pradeep sahu","Lisa") #Positional Argument.
greet(your_name= "Rose", name="pradeep sahu") #Keyword Argument.
